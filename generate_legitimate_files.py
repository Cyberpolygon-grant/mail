def create_realistic_pdf(title, content_lines):
    """–°–æ–∑–¥–∞–Ω–∏–µ –ø—Ä–∞–≤–¥–æ–ø–æ–¥–æ–±–Ω–æ–≥–æ PDF —Ñ–∞–π–ª–∞ —Å –ø–æ–¥–¥–µ—Ä–∂–∫–æ–π –∫–∏—Ä–∏–ª–ª–∏—Ü—ã"""
    
    def esc(txt: str) -> bytes:
        """–≠–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞ –¥–ª—è PDF: –∑–∞–º–µ–Ω—è–µ–º —Å–∫–æ–±–∫–∏ –Ω–∞ –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–µ –∏ –∫–æ–¥–∏—Ä—É–µ–º –≤ UTF-8"""
        return txt.replace("(", "[").replace(")", "]").encode("utf-8")
    
    header = b"%PDF-1.4\n%\xe2\xe3\xcf\xd3\n"
    objects = []
    
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º –ø—Ä–æ—Å—Ç—ã–µ —à—Ä–∏—Ñ—Ç—ã Type1 (Helvetica) - –æ–Ω–∏ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é—Ç UTF-8 —Å –∑–∞–º–µ–Ω–æ–π —Å–∫–æ–±–æ–∫
    font_helv = b"7 0 obj\n<</Type /Font /Subtype /Type1 /BaseFont /Helvetica>>\nendobj\n"
    font_helv_b = b"8 0 obj\n<</Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold>>\nendobj\n"
    objects.append(font_helv)
    objects.append(font_helv_b)
    
    # Page content stream
    page_stream_cmds = []
    
    # –ó–∞–≥–æ–ª–æ–≤–æ–∫
    page_stream_cmds.append(b"BT /F1b 20 Tf 72 780 Td (" + esc(title) + b") Tj ET\n")
    page_stream_cmds.append(b"0.6 w 72 770 m 540 770 l S\n")
    
    y = 750
    for line in content_lines:
        if line.strip():  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏
            page_stream_cmds.append((f"BT /F1 12 Tf 72 {y} Td (".encode() + esc(line) + b") Tj ET\n"))
        y -= 18
    
    page_stream = b"q\n" + b"".join(page_stream_cmds) + b"Q\n"
    page_obj = b"4 0 obj\n<</Length " + str(len(page_stream)).encode() + b">>\nstream\n" + page_stream + b"endstream\nendobj\n"
    objects.append(page_obj)
    
    # Page object
    page1 = b"3 0 obj\n<</Type /Page /Parent 2 0 R /MediaBox [0 0 595 842] /Contents 4 0 R /Resources <</Font <</F1 7 0 R /F1b 8 0 R>> >> >>\nendobj\n"
    objects.append(page1)
    
    pages = b"2 0 obj\n<</Type /Pages /Kids [3 0 R] /Count 1>>\nendobj\n"
    catalog = b"1 0 obj\n<</Type /Catalog /Pages 2 0 R>>\nendobj\n"
    
    ordered = [catalog, pages, page1, page_obj, font_helv, font_helv_b]
    
    # Build xref
    offsets = []
    pos = len(header)
    for obj in ordered:
        offsets.append(pos)
        pos += len(obj)
    xref = b"xref\n0 7\n0000000000 65535 f \n" + b"".join([f"{o:010d} 00000 n \n".encode() for o in offsets])
    trailer = b"trailer\n<</Size 7 /Root 1 0 R>>\nstartxref\n" + str(len(header) + sum(len(o) for o in ordered)).encode() + b"\n%%EOF\n"
    
    pdf_bytes = header + b"".join(ordered) + xref + trailer
    return pdf_bytes

def create_realistic_xlsx(title, data_rows):
    """–°–æ–∑–¥–∞–Ω–∏–µ –ø—Ä–∞–≤–¥–æ–ø–æ–¥–æ–±–Ω–æ–≥–æ XLSX —Ñ–∞–π–ª–∞"""
    
    excel_buffer = io.BytesIO()
    
    with zipfile.ZipFile(excel_buffer, 'w', zipfile.ZIP_DEFLATED) as xlsx:
        # [Content_Types].xml
        content_types = '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">
<Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>
<Default Extension="xml" ContentType="application/xml"/>
<Override PartName="/xl/workbook.xml" ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet.main+xml"/>
<Override PartName="/xl/worksheets/sheet1.xml" ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.worksheet+xml"/>
<Override PartName="/xl/sharedStrings.xml" ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.sharedStrings+xml"/>
</Types>'''
        xlsx.writestr('[Content_Types].xml', content_types)
        
        # _rels/.rels
        rels = '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="xl/workbook.xml"/>
</Relationships>'''
        xlsx.writestr('_rels/.rels', rels)
        
        # xl/_rels/workbook.xml.rels
        workbook_rels = '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/worksheet" Target="worksheets/sheet1.xml"/>
<Relationship Id="rId2" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/sharedStrings" Target="sharedStrings.xml"/>
</Relationships>'''
        xlsx.writestr('xl/_rels/workbook.xml.rels', workbook_rels)
        
        # xl/workbook.xml
        workbook = '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<workbook xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main" xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships">
<sheets>
<sheet name="–î–∞–Ω–Ω—ã–µ" sheetId="1" r:id="rId1"/>
</sheets>
</workbook>'''
        xlsx.writestr('xl/workbook.xml', workbook)
        
        # xl/sharedStrings.xml
        shared_strings_parts = ['<sst xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main" count="' + str(len(data_rows) + 1) + '" uniqueCount="' + str(len(data_rows) + 1) + '">']
        shared_strings_parts.append(f'<si><t>{title}</t></si>')
        for row in data_rows:
            shared_strings_parts.append(f'<si><t>{row}</t></si>')
        shared_strings_parts.append('</sst>')
        shared_strings = '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n' + ''.join(shared_strings_parts)
        xlsx.writestr('xl/sharedStrings.xml', shared_strings)
        
        # xl/worksheets/sheet1.xml
        worksheet_rows = ['<row r="1"><c r="A1" t="s"><v>0</v></c></row>']
        for i, row in enumerate(data_rows, start=2):
            worksheet_rows.append(f'<row r="{i}"><c r="A{i}" t="s"><v>{i}</v></c></row>')
        worksheet = f'''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<worksheet xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main">
<sheetData>
{''.join(worksheet_rows)}
</sheetData>
</worksheet>'''
        xlsx.writestr('xl/worksheets/sheet1.xml', worksheet)
    
    excel_buffer.seek(0)
    return excel_buffer.read()

def create_realistic_docx(title, content_lines):
    """–°–æ–∑–¥–∞–Ω–∏–µ –ø—Ä–∞–≤–¥–æ–ø–æ–¥–æ–±–Ω–æ–≥–æ DOCX —Ñ–∞–π–ª–∞"""
    
    docx_buffer = io.BytesIO()
    
    with zipfile.ZipFile(docx_buffer, 'w', zipfile.ZIP_DEFLATED) as docx:
        # [Content_Types].xml
        content_types = '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">
<Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>
<Default Extension="xml" ContentType="application/xml"/>
<Override PartName="/word/document.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.document.main+xml"/>
</Types>'''
        docx.writestr('[Content_Types].xml', content_types)
        
        # _rels/.rels
        rels = '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="word/document.xml"/>
</Relationships>'''
        docx.writestr('_rels/.rels', rels)
        
        # word/document.xml
        paragraphs = [f'<w:p><w:r><w:rPr><w:b/></w:rPr><w:t>{title}</w:t></w:r></w:p>']
        for line in content_lines:
            paragraphs.append(f'<w:p><w:r><w:t>{line}</w:t></w:r></w:p>')
        
        document = f'''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">
<w:body>
{''.join(paragraphs)}
</w:body>
</w:document>'''
        docx.writestr('word/document.xml', document)
    
    docx_buffer.seek(0)
    return docx_buffer.read()

def create_realistic_zip(files_data):
    """–°–æ–∑–¥–∞–Ω–∏–µ –ø—Ä–∞–≤–¥–æ–ø–æ–¥–æ–±–Ω–æ–≥–æ ZIP –∞—Ä—Ö–∏–≤–∞"""
    
    zip_buffer = io.BytesIO()
    
    with zipfile.ZipFile(zip_buffer, 'w', zipfile.ZIP_DEFLATED) as zip_file:
        for filename, content in files_data.items():
            zip_file.writestr(filename, content)
    
    zip_buffer.seek(0)
    return zip_buffer.read()

